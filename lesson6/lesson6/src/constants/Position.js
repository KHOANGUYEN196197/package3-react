const POSITION = [
    {
        id: 1,
        value: 'position1'
    },
    {
        id: 2,
        value: 'position2'
    },
    {
        id: 3,
        value: 'position3'
    },
    {
        id: 4,
        value: 'position4'
    }
]
export default POSITION;


// const POSITION = [...]: Đây là khai báo một hằng số (constant) POSITION và gán cho nó một mảng. Mảng này chứa thông tin về các vị trí công việc (position) với mỗi vị trí có một đối tượng chứa id và value. Có tổng cộng 4 vị trí công việc được định nghĩa trong mảng này.

// export default POSITION;: Cuối cùng, sau khi định nghĩa mảng POSITION, nó được xuất (export) ra ngoài module để có thể được sử dụng trong các tệp khác trong ứng dụng của bạn. Điều này cho phép các module khác có thể sử dụng danh sách các vị trí công việc này mà không cần định nghĩa lại.